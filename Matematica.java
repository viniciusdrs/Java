
public class Matematica {


		//Atributos locais
		int nota1,nota2;
		
		
		//Construtor da classe, que recebe dois valores (x e y) e atribui aos valores locais (a e b)
		public Matematica (int x , int y){
			nota1 = x;
			nota2 = y;
		}
			
		//Metodo de soma
			public int soma(){
				return nota1+nota2;
			}
				
				public int subtração(){
					return nota1-nota2;
				}
					
					public int multiplicação(){
						return nota1*nota2;
					}
						public int divisão(){
							return nota1/nota2;
			}
		}
	


